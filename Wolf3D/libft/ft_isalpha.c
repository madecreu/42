#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char	letter;

	letter = (unsigned char)c;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
